import { useState, useEffect } from 'react'
import { X, Sparkles, Loader2, Wrench } from 'lucide-react'
import ReactMarkdown from 'react-markdown'
import { Modal } from './Modal'
import { trackAIGenerated } from '../lib/analytics'

interface RepairGuideModalProps {
  vehicleLabel: string
  part: string
  onClose: () => void
}

export function RepairGuideModal({ vehicleLabel, part, onClose }: RepairGuideModalProps) {
  const [guide, setGuide] = useState<string | null>(null)
  const [isGenerating, setIsGenerating] = useState(false)
  const [guideError, setGuideError] = useState<string | null>(null)

  useEffect(() => {
    let cancelled = false

    const generateGuide = async () => {
      setIsGenerating(true)
      setGuideError(null)
      try {
        const parts = vehicleLabel.split(' ')
        const year = parts[0] || ''
        const make = parts[1] || ''
        const model = parts.slice(2).join(' ') || ''
        
        const res = await fetch('/api/ai/repair-guide', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ year, make, model, part })
        })
        
        if (!res.ok) {
          throw new Error('Failed to generate guide')
        }
        
        const data = await res.json()
        if (!cancelled) {
          setGuide(data.guide)
          trackAIGenerated(part, vehicleLabel)
        }
      } catch (err: any) {
        if (!cancelled) setGuideError(err.message || 'An error occurred')
      } finally {
        if (!cancelled) setIsGenerating(false)
      }
    }

    generateGuide()

    return () => {
      cancelled = true
    }
  }, [vehicleLabel, part])

  return (
    <Modal label={`AI Repair Guide for ${part}`} onClose={onClose} maxWidth="max-w-3xl">
      <div className="flex items-center justify-between border-b px-6 py-4 bg-slate-50 rounded-t-2xl">
        <div className="flex items-center gap-3">
          <div className="flex h-10 w-10 items-center justify-center rounded-xl bg-brand-100 text-brand-600 shadow-inner">
            <Sparkles size={20} />
          </div>
          <div>
            <h2 className="text-lg font-bold tracking-tight text-slate-900">AI Repair Guide</h2>
            <p className="text-xs text-slate-500">
              {vehicleLabel} • {part}
            </p>
          </div>
        </div>
        <button onClick={onClose} aria-label="Close" className="rounded-full p-2 text-slate-400 hover:bg-slate-200 hover:text-slate-700 transition-colors">
          <X size={20} />
        </button>
      </div>

      <div className="p-6 md:p-8 min-h-[50vh]">
        {isGenerating && (
          <div className="flex flex-col items-center justify-center py-20 text-slate-500">
            <Loader2 size={32} className="animate-spin text-brand-500 mb-4" />
            <p className="text-base font-medium text-slate-700">Gemini is writing your repair guide...</p>
            <p className="text-sm mt-2 text-slate-400">Analyzing service procedures for {vehicleLabel}</p>
          </div>
        )}
        
        {guideError && (
          <div className="flex flex-col items-center justify-center py-12 text-rose-600">
            <Wrench size={32} className="mb-4 text-rose-400" />
            <p className="text-lg font-medium">Failed to generate guide</p>
            <p className="text-sm mt-2">{guideError}</p>
            <button 
              onClick={onClose}
              className="mt-6 btn btn-secondary px-4 py-2"
            >
              Close and try again
            </button>
          </div>
        )}
        
        {guide && (
          <div className="prose prose-slate max-w-none prose-headings:font-bold prose-h1:text-2xl prose-h2:text-xl prose-h2:border-b prose-h2:pb-2 prose-h2:mt-8 prose-li:my-1 text-slate-700">
            <ReactMarkdown>{guide}</ReactMarkdown>
          </div>
        )}
      </div>
      
      {!isGenerating && !guideError && (
        <div className="border-t px-6 py-4 bg-slate-50 rounded-b-2xl flex justify-between items-center">
          <p className="text-xs text-slate-400">
            This guide is generated by AI. Always verify procedures and safety precautions with a professional service manual.
          </p>
          <button onClick={onClose} className="btn btn-secondary px-4 py-2 shrink-0 ml-4">
            Close Guide
          </button>
        </div>
      )}
    </Modal>
  )
}
